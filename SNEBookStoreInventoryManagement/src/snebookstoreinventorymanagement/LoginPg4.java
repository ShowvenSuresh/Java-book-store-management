    public void createAccount()
    {
        EmailVerify ev =new EmailVerify(LoginPg3.regEmail);
        if(ev.verifyCode()==true)
        {
            Login l = new Login();
            l.createAccount(LoginPg3.regUsername, LoginPg3.regPassword, LoginPg3.regFullName, LoginPg3.regEmail,LoginPg3.regAccType);
            LoginPg2 lp2 = new LoginPg2();
            lp2.setVisible(true);
            this.setVisible(false);
            this.dispose();
        }
        else 
        {
            LoginPg3 lp3 = new LoginPg3();
            lp3.setVisible(true);
            this.setVisible(false);
            this.dispose();
        }
        
    }
    
