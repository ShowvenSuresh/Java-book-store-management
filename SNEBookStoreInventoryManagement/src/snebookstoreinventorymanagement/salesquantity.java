    private void jPanel3MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel3MousePressed
     TableModel model1 = jTable1.getModel();                                    // Get the table model from jTable1
    int[] selectedRows = jTable1.getSelectedRows();                             // Get the selected rows from jTable1

    totalprice frm2 = new totalprice();                                         // Create an instance of the totalprice frame
    DefaultTableModel model2 = (DefaultTableModel) frm2.jTable1.getModel();

    int rowCount = model1.getRowCount();                                        // Get the row count of the table model
    float totalPrice = 0;                                                       // Initialize the total price variable

    boolean allValidNumbers = true;                                              // Flag to track if all numbers are valid
    for (int i = 0; i < rowCount; i++) 
    {
        String bookId = (String) model1.getValueAt(i, 0);
        String bookName = (String) model1.getValueAt(i, 1);
        String bookAuthor = (String) model1.getValueAt(i, 2);
        String bookPriceStr = (String) model1.getValueAt(i, 3);
        String quantityStr = (String) model1.getValueAt(i, 4);

        if (isValidNumber(quantityStr))                                         //Check if the quantity is a valid number
        {
            int quantity = Integer.parseInt(quantityStr);                       // Parse quantity string to integer
            if (quantity >= 0)                                                  // Check if quantity is non-negative
            {
                int stockQuantity = getStockQuantityFromDatabase(bookId);       // Get the stock quantity from the database
                if (stockQuantity >= quantity)                                  // Check if there's enough stock
                {
                    float bookPrice = Float.parseFloat(bookPriceStr);           // Check if there's enough stock
                    float subPrice = bookPrice * quantity;                      // Calculate subtotal price
                    totalPrice += subPrice;                                     // Update total price
                    Object[] rowData = {bookId, bookName, bookAuthor, bookPriceStr, quantityStr, subPrice}; // Create row data array
                    model2.addRow(rowData);
                }else
                    {
                         JOptionPane.showMessageDialog(null, "Not enough stock for book: " + bookName, "Stock Warning", JOptionPane.WARNING_MESSAGE);
                         allValidNumbers = false;                               // Set flag to false
                    }
            } 
        } else 
            {
                JOptionPane.showMessageDialog(null, "Please enter valid number!", "Error", JOptionPane.WARNING_MESSAGE);
                allValidNumbers = false;                                        // Set flag to false
            }
    }
    
    if(allValidNumbers)                                                         // If all numbers are valid
    {
        frm2.jTable1.setModel(model2);
        frm2.total.setText(String.valueOf(totalPrice));
        frm2.setVisible(true);
        this.setVisible(false);
    }
    }//GEN-LAST:event_jPanel3MousePressed

