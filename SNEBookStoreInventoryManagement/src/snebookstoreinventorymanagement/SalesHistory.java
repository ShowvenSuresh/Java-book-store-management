    public void table()
    {
        String searchText = search.getText();
        
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/snebookstore", "root", "admin");
            Statement s = con.createStatement();

	    String query = "SELECT * FROM salesrecord";                         // Query to retrieve all sales records
            ResultSet rs = s.executeQuery(query);

            while (rs.next())                                                   // Loop through the result set and populate the jTable with sales record data
            {
                String id=rs.getString(1);
                String name=rs.getString(2);
                String price=rs.getString(3);
                String date=rs.getString(4);  
                
                String tbData[] = {id,name,price,date};                         // Create an array with sales record data
                DefaultTableModel tblModel=(DefaultTableModel)jTable1.getModel();
                
                tblModel.addRow(tbData);                                        // Add the sales record data to the jTable

            }
        }catch(Exception ex)
                {
                    ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
    }

