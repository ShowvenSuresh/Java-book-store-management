private String getBookIdFromName(String bookName) 
{
    String bookId = "";
    try {
        Class.forName("com.mysql.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/snebookstore", "root", "admin");
        Statement statement = con.createStatement();
        String query = "SELECT bookId FROM bookinfo WHERE bookName = '" + bookName + "'";           // Query to retrieve book ID based on book name
        ResultSet bookRs = statement.executeQuery(query);
        if (bookRs.next())                                                                          // If book ID is found, assign it to bookId variable
        {
            bookId = bookRs.getString("bookid");
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    return bookId;                                                                                  // Return the book ID
}

private float calculateBookPrice(String bookName) {
    float price = 0.0f;                                                                             // Initialize price variable
    try {
        Class.forName("com.mysql.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/snebookstore", "root", "admin");
        Statement statement = con.createStatement();
        String query = "SELECT sPrice FROM bookinfo WHERE bookName = '" + bookName + "'";           // Query to retrieve the selling price of the book based on its name
        ResultSet rs = statement.executeQuery(query);
            if (rs.next()) 
            {                                                                                       // If selling price is found, assign it to the price variable
                price = rs.getFloat("sprice");
            }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    return price;                                                                                   // Return the price of the book
}



