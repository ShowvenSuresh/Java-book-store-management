    private void confirmMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmMousePressed
        boolean allInputsValid = true;                                          // Flag to track if all input quantities are valid
        
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/snebookstore", "root", "admin");
            Statement s = con.createStatement();

            LocalDate currentDate = LocalDate.now();                            // Get current date
            String returndate = currentDate.format(java.time.format.DateTimeFormatter.ofPattern("yyyy-MM-dd"));
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();   // Iterate through the rows of jTable1
            int row = model.getRowCount();
            for (int i = 0; i < row; i++)
            {                                                                   // Retrieve data from jTable1
                String bookName = (String) model.getValueAt(i, 1);
                String qb =(String) model.getValueAt(i,3);                      // Quantity bought
                String rs = (String) model.getValueAt(i, 4);                    // Return quantity
                String reason = (String) model.getValueAt(i,5);                 // Reason for return
                int returnQuantity = Integer.parseInt(rs);
                int purchaseQuantity = Integer.parseInt(qb);
                if (returnQuantity <=purchaseQuantity)                          // Check if return quantity exceeds purchase quantity
                { 
                    float price = calculateBookPrice(bookName);                 // Calculate book price

                    String insertReturnRecordQuery = "INSERT INTO returnrecord (returnPrice, returnDate) VALUES (" + (price * returnQuantity) + ", '" + returndate + "')";
                    s.executeUpdate(insertReturnRecordQuery);                   // Insert return record into returnrecord table

                    String bookId = getBookIdFromName(bookName);                // Get book ID from book name

                    String insertBookReturnQuery = "INSERT INTO bookreturn (returnid, bookid, reason, returnQuantity) VALUES (LAST_INSERT_ID(),'" + bookId + "', '" + reason + "', " + returnQuantity + ")";
                    s.executeUpdate(insertBookReturnQuery);                     // Insert return record into bookreturn table

                    String updateBookInfoQuery = "UPDATE bookinfo SET quantity = quantity + " + returnQuantity + " WHERE bookid = '" + bookId + "'";
                    s.executeUpdate(updateBookInfoQuery);                       // Update book quantity in bookinfo table
                }
                else 
                    {
                        JOptionPane.showMessageDialog(null, "Return quantity cannot exceed purchase quantity for book: " + bookName, "Error", JOptionPane.ERROR_MESSAGE);
                        allInputsValid = false;
                    }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

        if (allInputsValid)                                                     // Check if all input quantities are valid
        {
            JOptionPane.showMessageDialog(null, "Return records inserted successfully into the database.");
            BookReturn br = new BookReturn();                                   // Switch to BookReturn frame
            br.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_confirmMousePressed

