    private void jPanel2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MousePressed
TableModel model1 = jTable1.getModel();
saleshistorybook frm2 = new saleshistorybook();
DefaultTableModel model2 = (DefaultTableModel) frm2.jTable1.getModel();

int rowCount = model1.getRowCount();
boolean atLeastOneSelected = false;

for (int i = 0; i < rowCount; i++)                                              // Iterate through each row of the jTable
{
    Boolean isSelected = (Boolean) model1.getValueAt(i, 4);
    if (isSelected != null && isSelected) 
    {                                                                           // Check if the book is selected 
        String salesId = (String) model1.getValueAt(i, 0);                      // Get the sales ID of the selected book

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/snebookstore", "root", "admin");
            Statement s = con.createStatement();
            String query = "SELECT sr.salesID, bi.bookName, bi.bookAuthor, b.quantity, bi.sPrice " +    // Query to retrieve sales history data for the selected book
                    "FROM salesrecord sr " +
                    "LEFT JOIN booksales b ON b.salesID = sr.salesId " +
                    "LEFT JOIN bookinfo bi ON bi.bookId = b.bookId " +
                    "WHERE sr.salesID = '" + salesId + "'";

            ResultSet rs = s.executeQuery(query);                               // Execute the query
            while (rs.next()) 
            {
                String id = rs.getString(1);
                String name = rs.getString(2);
                String author = rs.getString(3);
                int quantity = rs.getInt(4);
                float price = rs.getFloat(5);
                float subtotal = quantity * price;

                String[] rowData = {id, name, author, String.valueOf(quantity), String.valueOf(subtotal)};       // Add the retrieved data to the model of the sales history form
                model2.addRow(rowData);
            }
            atLeastOneSelected = true;                                          // Set flag to indicate at least one book was selected
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
}

if (atLeastOneSelected)                                                         // Show the sales history form if at least one book was selected
{
    frm2.setVisible(true);
    this.setVisible(false);
} else {
    JOptionPane.showMessageDialog(null, "Please select at least one book", "Error", JOptionPane.WARNING_MESSAGE);
}
    }//GEN-LAST:event_jPanel2MousePressed
    
    private void applyDateFilter() 
    {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();           // Get the table model
    TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);     // Create a TableRowSorter
    jTable1.setRowSorter(sorter);                                               // Set the TableRowSorter to the jTable

    Date selectedDate = datechooser.getDate();                                  // Get the selected date from the date chooser
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");           // Define the date format
    String dateString = dateFormat.format(selectedDate);                        // Format the selected date as a string
    RowFilter<DefaultTableModel, Object> filter = RowFilter.regexFilter(dateString, 3);     // Create a RowFilter to match rows with the selected date in the specified column index 
    sorter.setRowFilter(filter);                                                // Set the RowFilter to the TableRowSorter
    }
    
