    public static int getStockQuantityFromDatabase(String bookId)
    {
        int stockQuantity =0;                                                   // Initialize stock quantity to 0
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/snebookstore", "root", "admin");
            Statement s = con.createStatement();
            
            String query ="SELECT quantity FROM bookinfo WHERE bookid ='"+ bookId +"'"; // Execute query to retrieve quantity from bookinfo table
            ResultSet resultSet=s.executeQuery(query);
            
                if(resultSet.next())                                             // If result set has a row, retrieve quantity and assign to stockQuantity
                {
                    stockQuantity = resultSet.getInt("quantity");
                }
        }catch(Exception ex)
            {
                ex.printStackTrace();
            }
        return stockQuantity;                                                   // Return the stock quantity of the book
    }
    
private boolean isValidNumber(String str) 
{
    if (str == null || str.isEmpty()) {
        return false;
    }
    return str.matches("\\d+");
}
